const tabela = document.getElementById("exames");

function preencherTabela(lista) {
    tabela.innerHTML = "";
    lista.forEach((item, i) => {
        tabela.innerHTML += `
        <tr>
            <td>${i + 1}</td>
            <td style="white-space: pre-wrap;">${item}</td>
        </tr>`;
    });
}
function preencherTabelaAutorizacao(lista) {
    tabela.innerHTML = `
      <tr>
        <th>Convênio</th>
        <th>USG / RX / Densi</th>
        <th>RM / TC </th>
        <th>Cemupe </th>
      </tr>
    `;

    lista.forEach(plano => {
        tabela.innerHTML += `
          <tr>
            <td>${plano.nome}</td>
            <td>${plano.usg_rx_densi}</td>
            <td>${plano.rm_tc}</td>
            <td>${plano.cemupe}</td>
          </tr>
        `;

    });
}

